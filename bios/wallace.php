		<h3>A perennial figure in the state’s education system, Dr. Phillip Wallace joined TNRMT as Trust Administrator in 2014 after retiring as the Director of Stewart County Schools.</h3>

		<h3>Dr. Wallace began his storied career as a teacher and football and basketball coach in Stewart County in 1969. He then spent 14 years as the Principal of Dover Elementary School and then another 22 years as the Director of Schools for the Stewart County Board of Education. He also has served as an adjunct professor at Christian Brothers University, Austin Peay State University, and Cumberland University.</h3>

		<h3>In 2005, Wallace experienced firsthand how the Trust helps state educators when a student in his school district shot and killed a school bus driver. During the aftermath that followed, Wallace says the Trust was there every step of the way, offering assistance to everyone who was affected by the tragedy.</h3>

		<h3>Now, as an employee of the Trust, he’s passionate about providing that same courteous, timely, and accurate service to clients and members. In addition to serving as CEO and executing the policies of the Trust, he’s charged with informing and advising the Board regarding Trust activities, employing and directing staff members, and maintaining adequate financial records.</h3>

