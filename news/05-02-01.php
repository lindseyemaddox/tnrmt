	<p>One of the most popular features of the Board liability program is the Athletic and Student Accident program.</p>

	<p>It provides benefits not only for parents, but your schools as well. As a result of your boardâ€™s participation in the Risk Management Trust, the parents of injured students in your schools receive benefits that supplement their primary health coverage at no cost to them.</p>

	<p>For students who do not have health care coverage, the plan becomes the primary policy paying benefits directly to medical providers to the limits of coverage of the Athletic and Student Accident policy.</p>

	<p>Your schools benefits from the program because parents do not have to prove negligence in order to receive medical benefits. That helps to reduce your liability exposure and avoid needless lawsuits against the school district and board.</p>

	<p>You can help make this program even better. The Risk Management Trust processes 1400 Student Accident claims annually. Prompt reporting of accidents is an important part of the success of the program since it allows the claims staff to quickly demonstrate to parents your concern for their child. Upon receipt of an accident notice, the adjuster immediately sends an acknowledgement letter to the parents advising them that a claim has been filed and identifies the information needed to process the claim.</p>

	<p>Please report all claims as promptly as possible through the Trust website: http://www.tnrmt.com. Do not wait until medical bills are received to report, and do not rely on the medical provider to report a claim. All claims should be reported via the web using the claim reporting form found there.</p>

