	<h1>Risk Management Trustees Elected</h1>

	<h3>June, 2005</h3>

	<p>Three new trustees were elected and one trustee re-elected in the first election of trustees by the membership of the Risk Management Trust. The elections were held as a result of overwhelming approval of a new intergovernmental agreement which called for trustees to be elected by the trust membership instead of being appointed by the president of TSBA. The intergovernmental agreements call for elections in the state to be divided into nine districts and a trustee to be elected from each district by the members in that district. Elections occur as trustee terms expire. Three trustees are elected each year for three year terms. Following is a brief sketch about each trustee elected this year.</p>

	<h4>District 4: Billy Joe Evans</h4>

	<p>Billy Joe Evans was elected to a three year term representing District 4. He is the superintendent from Fayetteville City Schools and has served in this position for the past 17 years. He also has served as president of the TSSAA Legislative Council and as the secretary of the TOSS Board. He has been a school administrator since 1972. Billy Joe is the second superintendent to serve on the trust board of trustees and the first to be elected by the membership. (Edwin Garrett, former superintendent of Overton County was the first.)</p>

	<h4>District 5: Mark Farley</h4>

	<p>Mark Farley was elected to a two year term from District 5. He was first appointed to the board of trustees in 2003. Mark is the Director of Finance for White County Government. He has served on the White County Board of Education since 1996 and served as a member of the TSBA Board of Directors as Upper Cumberland District Director for three years. His term is two years in order to make staggered terms work out so that three trustees are elected each year.</p>

	<h4>District 6: Joe Treece</h4>

	<p>Joe Treece is a new trustee elected to a three year term from District 6. He is a member of the Wayne County Board of Education. He has served on this board for 28 years and has been chairman for twelve years. He is president of Joe Treece Enterprises, Inc. which is a manufacturing company that has been in business for 42 years. He also served on the Clifton City Council for six years.</p>

	<h4>District 9: Larry Griffin</h4>

	<p>Larry Griffin is the first county mayor elected to serve as a trustee for the Risk Management Trust. He has served as Crockett County mayor since 2002. He also has served on the Emergency Medical Services Board for 11 years and served as the Director of Emergency Services for Crockett County for 30 years. He was named Crockett County Chamber of Commerce Man of the Year in 1999.</p>

