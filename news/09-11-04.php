	<h1>Kentucky Coach vs Kentucky Principal</h1>

	<h2><a href="/bios/henderson.php" class="fancybox fancybox.iframe">Harrold Henderson</a>, Tennessee Risk Management Trust</h2>

	<h3>November, 2009</h3>

	<p>In a previous issue of InTrust, we advised that TSSAA Board of Control passed a proposal that required a heat policy for all its member schools. The heat policy prohibits schools from practicing when the heat index at the location of activity was in excess of 104 degrees Fahrenheit. Other states high school athletic governing boards may quickly implement a similar policy as that of the TSSAA, especially with the recent highly profiled criminal case of a former Kentucky high school football coach charged in the death of a player, who collapsed at a football practice.</p>

	<p>Many of you will recall the former Kentucky high school football coach was the first coach charged in the death of a player. Several experts testified that the player suffered from exertional heat stroke, which led to his death. The case which ended earlier this month with the former coach found not guilty was closely watched by those involved in youth athletics across the country. Defense medical experts were able to show the jury a combination of heat, the use of the dietary supplement Creatine and medication for an underlying health condition as well as the player being ill before practice were the primary factors that contributed to the playerâ€™s death, which the court ruled as accidental.</p>

	<p>Get to know your players and know what medication they are on and when they are to take them. Monitor the practice and allow periods of rest when the player asks for a break.</p>

