	<h1>Time to Post OSHA 300A Reports</h1>

	<h2><a class="fancybox fancybox.iframe" href="/bios/stites.php">Jason Baggett</a>, Loss Control Specialist, Tennessee Risk Management Trust</h2>

	<h3>February, 2015</h3>

	<p>All of our members are required to complete an OSHA 300 Log. The OSHA 300 Log is a record keeping tool designed to track and record workplace injuries. In order to complete an OSHA 300 Log, we must first know what incidents are reportable. First, the injury or illness must have occurred at the workplace or be due to workplace activity. The incident must be serious, resulting in a fatality, loss of consciousness, time away from work, a restricted work schedule, or restricted work duties. You must also report any significant workplace event that leads to a health professional’s diagnosis, or medical removal from the job. Any needle-stick or cut that may involve contaminated blood is reportable. A positive tuberculosis test is reportable as well.</p>
	
	<p>When a reportable incident occurs, employers must log the incident on the OSHA 300 Log. Note that employers have seven calendar days to complete the OSHA 300 log once they receive notice of an injury, and should keep a record of all incidents as soon as they happen. Protocols must be in place to ensure these records and logs stay up to date throughout the year. Keep in mind that employers must be able to produce the logs whenever needed. These records must be kept for five years following the year they occur. During that time, update the OSHA 300 Log to include new incidents or to show changes in previous incidents. For more information regarding OSHA’s recordkeeping rules, please see <a href="https://www.osha.gov/recordkeeping/entryfaq.html" target="_blank">this webpage</a>.</p>
	
	<p>In addition to the OSHA 300 Log, another form required is the OSHA Form 300A. The OSHA Form 300A is the summary of work-related injuries and illnesses. At the end of each calendar year, employers must complete the form and have it certified by a company executive, such as a city manager, human resources director, etc., as accurate. Employers must post the OSHA Form 300A between February 1st and April 30th. Even if there were no recordable injuries or illnesses, employers must post the form with zeros on the total line. The form must be posted in a common area where employees have access. It is recommended to post the summary form where other internal notices and health and safety information is posted.</p>

	<p>Please note that the TNRMT staff does not capture lost work days. The TNRMT staff only tracks information pursuant to TN workers comp statute. However we help by providing a report indicating workers compensation injuries that are reported to us. As a result, accurate record keeping is a responsibility of our members, especially when it comes to tracking days away from work due to an injury. Employees that continued to work at light duty must also be documented.</p>
	
