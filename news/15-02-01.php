	<h1>Concussion Awareness</h1>

	<h2><a class="fancybox fancybox.iframe" href="/bios/stites.php">Chris Stites</a>, Safety Engineer, Loss Control Specialist, Safety Engineering &amp; Claims Management<br /><a class="fancybox fancybox.iframe" href="/bios/baggett.php">Jason Baggett</a>, Loss Control Specialist, Tennessee Risk Management Trust</h2>

	<h3>February, 2015</h3>

	<p>No doubt about it, sports are a great way for kids to stay healthy while learning important team-building skills.  What are the risks of pushing the limits of speed, strength and endurance? Children, parents and without a doubt physical education teachers & team sport coaches, must be aware of signs and symptoms of concussion.</p>
	
	<ul>
		<li>There were 120 sports-related deaths of young athletes in 2008-2009; 50 in 2010; and 40 in 2011.</li>
		<li>There are three times as many catastrophic football injuries among high school athletes as college athletes.</li>
		<li>62% of organized sports-related injuries occur during practices.</li>
	</ul>
	
	<p>CONCUSSION</p>
	
	<ul>
		<li>50% of "second impact syndrome" incidents - brain injury caused from a premature return to activity after suffering initial injury (concussion) - result in death.</li>
		<li>Female high school basketball players suffer 240% more concussions than males (13,000).</li>
		<li>400,000 brain injuries (concussions) occurred in high school athletics during the 2008-09 school year.</li>
		<li>Emergency department visits for concussions sustained during organized team sports doubled among 8-13 year olds between 1997 and 2007 and nearly tripled among older youth.</li>
		<li>More than 248,000 children visited hospital emergency departments in 2009 for concussions and other traumatic brain injuries related to sports and recreation.</li>
		<li>Injuries associated with participation in sports and recreational activities account for 21% of all traumatic brain injuries among children in the United States</li>
	</ul>
	
	<p>Tennessee law makers heard the cry in April of 2013. Tennessee passed a sports concussion law designed to reduce youth sports concussions and increase awareness of traumatic brain injury. Public Chapter 148, has three key components:</p>
	
	<ol>
		<li>To inform and educate coaches, youth athletes and their parents and require them to sign a concussion information form before competing.</li>
		<li>To require removal of a youth athlete who appears to have suffered a concussion from play or practice at the time of the suspected concussion.</li>
		<li>To require a youth athlete to be cleared by a licensed health care professional before returning to play or practice.</li>
	</ol>
	
	<p>Both public and private school sports and recreational leagues for children under age 18 that require a fee are affected by the new law. The law covers all sports.</p>

	<p>One such way we can ensure proper education is being conducted is to annually require a re-certification of coaches as well as anyone who is involved in instruction of physical activities. The CDC offers a free online training course which provides proof of completion. We highly recommend all our entities ensure adherence to not just the law but the safety of our kids.</p>
	
