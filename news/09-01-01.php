	<h1>How to Eliminate Lawsuits</h1>

	<h2>January 2009</h2>

	<p>The headline above would imply that there is a way to eliminate lawsuits. Unfortunately, there is no way to avoid lawsuits but there are several proactive steps managers, officials and administrators can take to reduce judgments and damage awards when lawsuits occur.</p>
	
	<p>Unquestionably, there is an ever increasing amount of litigation directed at all segments of government operations these days. Years ago the idea that someone would bring a lawsuit against a school or local government was a rare thought indeed. Today, local governments, schools and their employees are on the receiving end of claims that range from personal injuries to discrimination. Some of these claims are federal in nature and seek damages well beyond the boundaries of the Tennessee statutory tort caps set forth in the Governmental Tort Liability Act. There are even claims directed against individual employees, some of which are unfortunately directed against Directors of Schools, County or City Mayors and managers. Is it any wonder that today's local government or school CEO feels pressured to serve as part-time administrator and full-time attorney?</p>

	<p>While no one expects managers and administrators to serve as full-time attorney, here are some basic reminders to assist you. The basics are awareness, documentation, and reporting.</p>

	<ul>
		<li>Challenge employees to be aware of that which does not look right. It is a basic approach but one that is practically useful. If a fellow-employee or staff member is engaging in some activity that seems inappropriate, your employees need to see it and take notice of it;</li>
		
		<li>Once your employees take notice of the activity, challenge them to document it. An employee who is seemingly engaged in inappropriate conduct, whether the conduct is directed toward a student, fellow employee, subordinate, or any other person, must have his or her actions documented; and,</li>
		
		<li>Once documented, challenge your employees to report the activity to the appropriate administrator. Then, the administrator is enabled to make the appropriate response. The meaning of “appropriate response” varies widely. In some instances an investigation will be necessary, and in other instances corrective measures must be implemented. That is when a competent administrator takes over and makes an immediate and appropriate decision to mitigate any potential fallout from action or inaction of fellow administrators or employees.</li>
	</ul>

	<p>Following these three simple steps will drastically reduce the implications and cost of any potential lawsuit, saving trouble and money for both your entity and your Risk Management Trust. Again, you do not have to serve as fulltime lawyers; just teach your employees to be aware, to document, and report to the appropriate administrator, who will in turn provide a prompt and effective response. Following these basic steps will not end the litigation, but your implementation of them will greatly assist you and your local government or schools in defending any lawsuit.</p>

