	<h1>Reappraisals finding missing, undervalued buildings</h1>

	<h3>September, 2006</h3>

	<p>The TNRMT appraisal program with Risk Assurance Inspections (RAI) is progressing as expected, according to John Wilburn, administrator of the TNRMT.</p>

	<p>RAl has completed appraisals on 20 of the trust's members and will complete eight more by the end of the year. Comments from members have been positive regarding the professionalism of the appraisal staff.</p>

	<p>In January 2006, TNRMT embarked on a three-year project to appraise the property of its entire membership. The objectives were threefold:

	<ul>
		<li>First was the obvious need of trust members to know the replacement value of their buildings, allowing both the school system and the trust to be sure they were properly insured in the event of a total loss.</li>
		<li>Second was the need of TNRMT to be accurate and fair in establishing premiums for replacement cost coverage.</li>
		<li>Lastly was the requirement of the Comptroller to meet the terms of GASB 34. At the end of the three-year program, all program objectives will be met.</li>
	</ul>
	</p>

	<p>The appraisal contractors, RAI, will update appraised values each year to be sure the established values are maintained. All appraisals become inaccurate as the effects of the economy and the costs of labor and building materials change over time.</p>
	
	<p>The appraisal results show that while some buildings were overvalued, many more were undervalued.  Overall, buildings covered by TNRMT appear to be undervalued by an average of approximately 17 percent. The TNRMT currently covers as much as 125 percent of the scheduled value of covered structures to offset this shortfall.</p>

	<p>However, what is more important, said Wilburn, is the trust has found buildings that werenâ€™t listed at all while other buildings were undervalued as much as 50 percent. This would be a difficult margin to overcome in the event of a total loss to a building, he said.</p>

	<p>"We will be offering additional services to evaluate and record the contents values of the membership in the near future," Wilburn said. "RAI is in the development stage of a database in which members can access and record their categorized contents values. Once this data is logged, the database will automatically keep annually depreciated values calculated. This service will be offered at no extra cost to the members. We are sure you will want to use this new service for maintenance of GASB 34 requirements as soon as it is available."</p>


