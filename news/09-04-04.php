	<h1>When Disaster Strikes</h1>

	<h2>April 2009</h2>

	<img src="/_images/news/wilburn-check.jpg">

	<p>Within days of its dedication, fire completely destroyed the Lily Pad Research Center in Norris, Tennessee. Principal Jess Anne Cole said, “We were devastated. So much work went into the facility, so many people and businesses had a hand in helping us realize the dream of a regional science center to go with our wetlands project.”</p>

	<p>John Byrd, who is with the Anderson County Schools science program said, “The building was a symbol of the community working together, and that symbol has been damaged, but the wetlands are intact and our work will go forward.”</p>

	<p>Jim Woodard, Anderson County Schools’ business director, indicated that the building had so much donated in terms of labor and materials and that he was not sure how it all would be handled. He further commented, “We will have to work through that.”</p>

	<img src="/_images/news/lilypad.jpg">

	<p>“Fortunately,” said retiring Director V. L. Stonecipher, “our insurance coverage is with the Risk Management Trust. They have indicated that the building is covered for its replacement value.” Stonecipher went on to say that the Trust has always responded very promptly and very fairly to their insurance needs.</p>

