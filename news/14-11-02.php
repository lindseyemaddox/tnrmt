	<h3>November, 2014</h3>

	<p>Throughout the year, you will likely have the need to request a change in your entity’s Property Schedule or Auto Schedule, request Builders Risk Coverage, or even request a Certificate of Insurance (COI).  If you’re a member of the Tennessee Risk Management Trust, then all of these scenarios can be handled quickly and easily on the NGU Risk Management website.</p>
 
	<p>First, go to <a href="http://www.ngutn.com/forms" target="_blank">www.ngutn.com/forms</a>.  You’ll notice four options: one for the Builders Risk Coverage Application, one for Property addition/deletion, one for Vehicle addition/deletion, and one option to request a Certificate of Insurance (COI).  Just click the appropriate button and fill out the form.  Upon submission, you and the NGU Risk Management staff will receive a copy of the request by email.  At that point, NGU Risk Management staff members will act upon the request very quickly and follow up with you if needed.  Note that for Property / Vehicle schedule change requests, you can enter up to five change requests per form.</p>

