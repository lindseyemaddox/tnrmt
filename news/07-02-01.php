	<h1>Teach the Newbie</h1>

	<h2>Adequately orient new employees to limit job injuries</h2>

	<h3>February, 2007</h3>

	<p>Insurance studies indicate that most job injuries occur to new employees because of the unfamiliar work environment and a lack of safety training for the new job. With this in mind, organizations can reduce workers' compensation cost by adopting a new employee safety orientation.</p>

	<p>A good approach? Use a thorough checklist.</p>

	<p>A supervisor should conduct safety orientation the first day of employment.</p>

	<p>Have the supervisor and new employee sign the checklist to verify items that were covered during orientation. The signed copy should be kept in the employee's personnel file. No item is too small to be included.  Below are some examples of items that should be on the checklist:

	<ul>
		<li>Discuss safety policies and procedures with the new employee and give a copy to the employee</li>
		<li>Review accident reporting procedures and stress prompt reporting</li>
		<li>Demonstrate safe handling of any hazardous materials. Demonstrate lockout/tagout procedures on equipment the new employee will use</li>
		<li>Review personal protective equipment and its proper use</li>
		<li>Impress upon the new employee the organization's commitment to safety, the fact that accidents can be prevented, and what to do when unsafe conditions are observed.</li>
	</ul>
	</p>

	<p>The examples above are only a few ideas of what should be included on a new employee orientation checklist.</p>

	<p>By taking the initiative to reduce accidents, members can reduce turnover, increase productivity, increase morale and help the trust save on workers' compensation costs.</p>

	<p>Safety-related questions or concerns can be directed to the trust's loss control consultants.</p>


