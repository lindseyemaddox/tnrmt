	<h1>Property appraisal program to measure every structure</h1>

	<h3>May, 2006</h3>

	<p>An ambitious program to appraise property values in all Tennessee Risk Management Trust member school systems over the next three years is under way, and appraisers are measuring every stick and stone.</p>

	<p>Jeremy Shupe, with Risk Assurance Inspection of Brentwood, said appraisers will spread their time among school systems in all three grand divisions of Tennessee over the next three years. What is covered in the property appraisals? Everything that could conceivably fall under the category of real estate.</p>

	<p>Shupe said that in addition to the classroom buildings and administrative offices, "We are looking at everything from playground equipment to walk-in coolers to pavilions-all the stuff sticking out of the ground."</p>

	<p>Cumberland County was first on the list.</p>

	<p>"They seemed to be pretty thorough," said Cindy Randol, Cumberland County Schools business manager. "They measured the play areas and outside areas, parking lots and even property that we have not built on."</p>

	<p>The appraisals will help TNRMT know exactly how much it would cost to replace each structure and therefore how much it should be insured for.</p>

	<p>"We just go in and tell you how much it would cost to replace it, as compared to how much you've been insuring them for," Shupe said. "This will let Risk Management Trust know how much possible risk you have out there."</p>

	<p>He added that replacement cost may be very different from what it cost to build a building in the first place.</p>

	<p>"What would it cost to replace it if it were blown away?" he said. "What if the price of steel doubles in a week, and all of a sudden you're $3 million short? It's not how much that pile of bricks cost, but how much a new one would cost."</p>

	<p>Shupe said the appraisers will contact each member system's school superintendent or financial manager to set up the visits a few days in advance 'We will go and visit every property onsite."</p>

	<p>The process includes measuring the "underroof' square footage of every structure. He said it would help if each school system could determine in advance exactly what it owns, because in some instances a building may be jointly owned by the city and county or by the county and the school system.</p>

	<p>The Trust will bear the cost of the appraisals, which will be used to make sure members have adequate insurance coverage and that they are treated fairly.</p>

	<p>Shupe added that one of the lessons of Hurricane Katrina was that people in the affected areas realized they did not have enough insurance on buildings, and insurance companies had problems because so many properties were wiped out all at once.</p>


