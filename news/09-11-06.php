	<h1>Cell Phones and Text Messaging</h1>

	<h2><a href="/bios/wilburn.php" class="fancybox fancybox.iframe">John Wilburn</a>, Trust Administrator</h2>

	<h3>November, 2009</h3>

	<p>Is this a problem in your school? If the response is yes, perhaps you question what you can do to stop it. The Attorney General says you can legally confiscate a student’s cell phone, even one actually belonging to the parent.</p>

	<p>Pursuant to the Student and Employee Safe Environment Act of 1996, local education agencies are “responsible for formulating a code of acceptable behavior and discipline to apply to students.” Thus, you can adopt a policy regarding possession and use of cell phones on school property as well as disciplinary actions taken for violation of the policy. Such actions can include suspension as well as temporarily seizing the device, but seizing the device does not imply consent to examine the call log, photographs or text message.</p>

	<p>Examination of the device would require parental consent, or in the case of texting, consent of one of the parties to the text message in order to avoid the allegation of invasion of privacy. If consent is not given, we would recommend the SIMM card be removed and given to the parent until the end of the disciplinary period.</p>
	
	<p>Most importantly in this or any other disciplinary process, be sure that you have fully investigated the matter and given the accused a chance to explain and that the entire process has been thoroughly documented.</p>

