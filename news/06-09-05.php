	<h1>Return-to-work benefits employer, injured employee</h1>

	<h3>September, 2006</h3>

	<p>What is a Return-to-Work Program? A return-to-work (RTW) program is a proactive process used by the employer to help employees who have suffered a work-related injury or illness to return to their previous economic, social and vocational status. The goal of a RTW program is to get the injured worker back to work as quickly as possible. To accomplish this goal, it may be necessary to make a short-term modification to the work schedule or job duties to accommodate restrictions imposed by the physician.</p>

	<h4>Benefits</h4>

	<p>An effective RTW program benefits both management and employees as it:

	<ul>
		<li>Discourages malingering, and reduces the chance that the employee will never return to the workplace</li>
		<li>Reduces the disability associated with an injury and its related cost</li>
		<li>Helps to avoid the alienation that can occur on both sides with a prolonged absence from the job</li>
		<li>Improves morale of all employees</li>
		<li>Minimizes legal involvement</li>
	</ul>
	</p>

	<h4>Steps to a Successful Program</h4>

	<p>For a RTW program to be successful, the employer must make a firm commitment to adopt and adhere to a RTW program and ensure that everyone involved understands their role in the RTW process.</p>

	<p>RTW is a team effort, and requires training for employees and supervisors about what to do when an injury occurs as well as training to advise about the benefits of the return-to-work program.</p>

	<p>Remember to report all accidents as soon as they occur. If no medical treatment is expected, simply indicate that you are reporting for records only.</p>

	<p>Claim reporting forms can be found on the tnrmt com website. All claims should be reported electronically.</p>

