	<h1>Few Boards Bid Insurance</h1>

	<h3>June, 2005</h3>

	<p>The Risk Management Trust is a pool of governmental entities operating a first-class risk management program. Most boards understand the concept because they continue their membership from year to year without bidding. They recognize that they have agreed to be a part of a pool of governmental entities owned and governed by the membership.</p>
	
	<p>Only nine of the 156 RMT members requested bids this year. We retained seven of the nine members who bid even though quotes for members bidding are sometimes as much as 25% higher because members who bid are excluded from the pool and priced separately without the advantages of group pricing on excess insurance purchased by the pool. That is why the legislature specifically granted governmental entities an exemption from the bidding requirement if the governmental entity participates in a pool for governmental entities.</p>

	<p>Advantages of participation in the Risk Management Trust include:

	<ul>
		<li>members have agreed to be a part of a pool of governmental entities which own the pool themselves.</li>
		<li>members are doing business with themselves.</li>
		<li>members who exercise their responsibility to all other members to control losses, are also controlling the cost of participation.</li>
		<li>members who bid are excluded from the pool and priced separately without the advantages of group pricing on excess insurance purchased by the pool.</li>
		<li>members who bid might get a lower premium for the first year but they will likely see an increase in the second and third years.</li>
		<li>members who help the pool earn a profit strengthen the pool and help to hold premiums down.</li>
		<li>members who continuously participate in the same high quality loss control and safety program reduce accidents and lower premiums.</li>
	</ul>
	</p>

	<p>In the long term, the RMT will be the best coverage for the lowest price of any risk management program available to Tennessee schools and governmental entities. We appreciate our faithful members who depend on the trust for your risk management needs. We pledge to provide service of the highest quality to your board and its employees and constituents.</p>

