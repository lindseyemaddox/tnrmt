	<h1>Three Trustees Leave Board</h1>

	<h3>June, 2005</h3>

	<p>Three Risk Management trustees have completed their terms and leave the board of trustees effective June 30, 2005.</p>
	
	<p>Gloria Sweet-Love is a member of the Haywood County Board of Education and is the president-elect of TSBA. She has been a trustee since 1993. </p>
	
	<p>Gordon Morris has been a member of the board of trustees since 2001. He is a member of the Weakley County Board of Education and immediate past president of TSBA.</p>
	
	<p>Darryl Deason has been a member of the board of trustees since 2001. He is a member of the Cannon County Board of Education. He is moving out of his district and leaving the board of education.</p>
	
	<p>Thank you Gloria, Gordon and Darryl for your service as Risk Management Trustees.</p>

