	<h3>August, 2014</h3>

	<p>The military draw down from Afghanistan has resulted in numerous surplus military vehicles being stockpiled, and in many cases these are available to other government agencies and authorities at no charge.</p>

	<p>TNRMT will provide liability coverage only for these vehicles.  We will be unable to provide physical damage coverage for them should they be damaged or destroyed.

	<p>The reason for this position is as follows:</p>

	<p>Typically these vehicles are provided to our members at no cost, and for the foreseeable future, they should continue to be available at no cost.  As you know, in the event of a covered loss, TNRMT vehicle coverage provides for the cost to repair or-in the event of a total loss-the actual cash value, whichever is less.</p>

	<p>As the replacement cost of these military vehicles is arguably zero since they are free when available-save perhaps some minimal transport charges-the ACV is arguably zero as well.  Parts and repairs generally are not available locally (if at all) and would likely prove to be very expensive. This is not a cost that TNMRT contemplates in its physical damage rate structure.</p>

	<p>So, we are happy to provide liability coverage at no charge for authorized use of these vehicles. If you have questions, please let us know.</p>

