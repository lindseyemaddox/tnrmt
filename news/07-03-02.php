	<h1>They're Back!!</h1>

	<h2>March 2007</h2>

	<p>Several bills of interest have been introduced this session which could have a tremendous effect on your TNRMT program in the coming years. The bills listed below, if passed as currently drafted, will result in a significant increase in the cost of liability insurance, a cost which could ultimately be passed on to Trust members. </p>

	<p>SB 0008 and HB 0027 would require governmental entities to be liable for all actual medical, pharmacy, and physical rehabilitation costs rendered against such entity even if such costs exceed the amounts currently required in the Governmental Tort Liability Act. This legislation was introduced by Senator Jackson and Representative Briley. SB 0038 and HB 0018 contain similar language and were introduced by Senator Jackson and Representative Shepard. </p>

	<p>You are aware that liability for damages is capped under the Governmental Tort Liability Act. (TCA 29-20-403) SB 0089 sponsored by Senator Cooper, and HB 0217 sponsored by Representative Odom proposes significant increases to the damage caps under the Act. </p>

	<p>The Trust is opposed to passage of these bills and will actively lobby against them. We encourage you to advise your Representatives and Senators that passage of these bills would have a significant financial impact on the cost of risk management to your school district, county, city or utility.</p>

