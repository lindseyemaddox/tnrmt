	<h1>A Team</h1>

	<h3>March, 2005</h3>

	<p>Perhaps you recall a successful television show called the A Team, and a saying from the lead character, “Don’t you just love it when a plan comes together.” Come together it did on February 21st in Dickson County. Tornado force winds struck the Dickson County High School just before 8:00 a.m. Fortunately, schools were closed for Presidents Day.</p>

	<p>By approximately 9:00 a.m., TSB-RMT learned of the catastrophe and within the hour, Adjuster Harrold Henderson was on site. Initial observations confirmed damages far exceeded TSB-RMT’s initial layer of coverage. We promptly alerted the reinsurer, and arranged to have their adjuster on site within 48 hours.</p>

	<p>Together, Harrold and the reinsurer authorized clean-up, and emergency repairs to avoid further damage to the building or its contents. Work crews were quickly assembled, some from as far away as Memphis, who worked well into the night to board up broken windows and cover the damaged roofs. Due to the efforts of all, further damage was avoided, and actual repair began on February 22nd, the day following the storm.</p>

	<p>On the same day, Director of Risk Management, John Wilburn, presented Director of Dickson County Schools, Charlie Daniels with a check representing the first layer of coverage allowing Dickson County to begin the cleanup without having to commit any their own funds.</p>

	<p>A plan did come together quickly due to prompt reporting from Dickson County officials, prompt action by our A Team Adjusters, Harrold Henderson, and Paul Latteau of St. Paul/Travelers insurance, allowing Dickson County High to safely reopen Friday, February 25th, four days after the storm, for regularly scheduled classes.</p>

	<p>It remains our distinct pleasure to serve the members of the TSB-RMT regardless of whether we are needed to provide an answer to a simple coverage question or handle a loss of the magnitude of the one in Dickson County Schools. We are here to help.</p>

