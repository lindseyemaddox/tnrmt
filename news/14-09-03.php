	<h1>FAQs - Transporting Athletes</h1>

	<h2><a class="fancybox fancybox.iframe" href="/bios/sullivan_b.php">Betsey Sullivan</a>, Customer Service Representative, NGU Risk Management</h2>

	<h3>September, 2014</h3>

	<p>There have been several school systems asking if the liability/insurance of a personally owned vehicle could be assumed by the school system.  This has been previously addressed in <a href="http://www.ngutn.com/faqs/" target="_blank">the Q&A Section of the NGU website</a>.</p>

	<h4>Previous Question & Answers are outlined below:</h4>

	<h4>Question 1:  </h4>

	<p>We sometimes allow a student to transport fellow students to various school approved activities using the student’s car.  Is the driver covered?  Are the student/passengers covered?</p>

	<h4>Answer:  </h4>

	<p>The student is neither an employee nor is he driving a school owned vehicle.  The student is “loaning” his car to the school and is therefore “loaning” his insurance much the same as the school system does when it “loans” a bus to local church groups.  The student/passengers would be covered by the owner’s insurance.  In this situation the school system would most likely have some contingent liability.  This would be covered by the TNRMT.  Needless to say, this practice is not good risk management.  </p>

	<h4>Question 2: </h4>

	<p>A parent wants to let a school employee use their personal vehicle to transport a golf team to a match.  Is there a form that will release the vehicle owner from liability in case of an injury to any of the passengers?</p>

	<h4>Answer:</h4>

	<p>No.  The owner of the vehicle cannot be released from liability.  In Tennessee, the insurance follows the vehicle.</p>

	<h4>Question 3: </h4>

	<p>A coach sometimes drives his own vehicle to ball games to haul necessary equipment.  Is his vehicle covered and is his liability covered for this operation?</p>

	<h4>Answer:</h4>

	<p>Since the coach is the owner and the operator of the vehicle identified above, even though he is on school business, he is not a named insured nor is his vehicle insured.  However, damage that he may do to property or person of others on school business is covered with respect to liability of the school system or its board or employees other than the coach.</p>

