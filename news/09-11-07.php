	<h1>Legal Corner: Guns Allowed in Public Parks, Except When Children Are Present</h1>

	<h2>Keith Dennen, Bone, McAllester, Norton</h2>

	<h3>November, 2009</h3>

	<p>Last year, the Tennessee General Assembly debated whether people who have permits should be allowed to carry handguns in public parks. In May 2009, the General Assembly passed a law allowing handguns in public parks. That law went into effect on September 1, 2009. If you have a carry permit, you can carry a handgun in a public park in Tennessee. However, as the old saying goes, “every rule has an exception.”</p>

	<p>First, the new law allows a city or county to ban guns in parks that it owns or operates. Tenn. Code Ann. § 39-17-1311(d). To do this, the city or county legislative body must simply pass a resolution banning handguns and post a sign at the park’s entrance.</p>

	<p>Second, the law applies only to parks “owned or operated” by a city or county. If a private organization, such as the Beech Community Association, owns the park, then the law still bans handguns even though the park is privately owned, but yet is publicly used.</p>

	<p>Third, the law did not repeal Tennessee Code Annotated section 39-17-1309. That law bans handguns in any public park or recreational facility during the time when the park or facility is being used for a school activity. This ban applies even if a sign has not been erected – the presence of the students for a “school” activity is enough. Thus, if a school baseball team is practicing on a baseball field in a public park – no guns are allowed.</p>
	
	<p>These exceptions create questions. Those questions include:
		<ul>
			<li>Can a school conduct activities in a park or play games in a park where guns are allowed? The answer is yes. The law bans guns in that park.</li>
			<li>If the park is 100 acres, does the ban apply to the entire park? The answer is yes.</li>
			<li>If I do not know that students are present for a school activity, am I allowed to carry my gun? The answer is no.</li>
		</ul>
	</p>

	<p>The moral of the story is that you may have a carry permit, but you may not be allowed to carry in a public park.</p>

	<p>The Tennessee Attorney General has even issued an opinion on this question – <a href="http://www.tn.gov/attorneygeneral/op/2009/op/op129.pdf" target="_blank">Opinion No. 09-129</a>.</p>

