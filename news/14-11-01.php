	<h1>Basics of Inspecting Playgrounds</h1>

	<h2><a class="fancybox fancybox.iframe" href="/bios/baggett.php">Jason Baggett</a>, Loss Control Specialist, Tennessee Risk Management Trust</h2>

	<h3>November, 2014</h3>

	<p>Every year, TNRMT has several reports of playground accidents.  Unfortunately, a few of these accidents are simply unavoidable.  However, with proper supervision and maintenance, most accidents can be eliminated.</p>

	<p>Playgrounds are often overlooked in regular maintenance and inspections.  This is not ideal because playgrounds are an ever changing environment.  The wear and tear of regular use by hundreds of children takes its toll on not only the equipment itself, but the ground surfacing as well.  Very often, playgrounds are built by PTO members or others in the community.  While their heart may be in the right place, these playgrounds often do not meet the Consumer Product Safety Commission (CPSC) guidelines.  It is very important that any new playground installed meets the CPSC guidelines as well as the manufacturerâ€™s guidelines for installation and maintenance.  These guidelines can be viewed at <a href="http://www.playgroundsafety.org" target="_blank">www.playgroundsafety.org</a>.  TNRMT also recommends putting together a committee in your entity for purposes of reviewing any new playground equipment proposals and ensuring that existing playgrounds meet the proper guidelines or recommendations.</p>
	
	<p>Listed below are some basic tips for daily inspections of playgrounds:
	<ul>
		<li>Always make sure kids are supervised on playground equipment</li>
		<li>Maintain at least 9 inches (preferably 12) of protective surfacing</li>
		<li>Ensure the ground surfacing is contained</li>
		<li>Make sure protective surfacing extends 6 feet  in all directions of playground equipment (for swings  make sure you have twice the height of suspending bar in front and back)</li>
		<li>Replace missing or damaged hardware and make sure all S hooks on swings are closed</li>
		<li>Make sure equipment is firmly anchored (give it the shake test)</li>
		<li>If playground equipment has moving parts make sure they are in good working order</li>
		<li>Look for rusty welds on equipment as this could result in the failure of equipment</li>
		<li>Look for exposed rods, bolts, screws or nails</li>
		<li>If any piece of playground equipment is damaged take it out of service immediately</li>
	</ul>
	</p>
	
	<p>If you have any questions about your playground equipment, or if you would like any of the loss control staff to take a look at your playground equipment, please do not hesitate to ask.  We would be more than happy to help you with a detailed inspection.</p>
	
