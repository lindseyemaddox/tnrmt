	<h1>It’s That Time Again!</h1>

	<h3>March, 2005</h3>

	<p>About the same time every year, you are asked to complete a renewal application and verify property, inland marine, and auto schedules. And if you have been a member of the pool for several years, you have probably asked yourself “why” more than once. “Is it really worth my time?”</p>

	<p>While the TSB-RMT and its collective members share the financial risk of claims, the underwriting decisions and resulting annual contributions are based on the individual member’s own unique exposures, values, and loss history. With ever present budget crunches faced by political subdivisions, it is important to you and TSB-RMT that your annual contributions are based on accurate, timely and current information.</p>

	<p>Underwriting involves the review and analysis of a member’s risk, based on its exposures and loss experience for the member’s acceptability or continued participation in the TSB-RMT. The process also includes recognizing those members who can improve their loss experience with loss control service and training of employees or rejection of those risks who do not meet underwriting standards or refuse to participate in loss control assistance offers from TSB-RMT.</p>

	<p>The base rates for establishing a member’s annual contribution are established through actuarial studies of exposure to loss vs. actual loss experience of all TSBRMT members. Those rates are then applied toward your specific exposure to loss: property values, number and type of vehicles, number of students (school systems), and gross operating expenditures. Your budget not only provides the expenditures, but also can identify any unique exposures that you may have or which need particular attention.</p>

	<p>The stability of your TSB-RMT program and its ability to pay claims is dependent upon equitable premiums for exposures. Your current information insures that stability. </p>

	<p>What do you think? Is it worth your time?</p>

