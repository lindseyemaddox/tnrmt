	<h1>TNRMT Membership Not Bashful About Renewal</h1>

	<h2><a href="/bios/evans.php" class="fancybox fancybox.iframe">John Evans</a>, NGU Risk Management</h2>

	<h3>November, 2009</h3>

	<p>Another July renewal is now behind us. This is the 22nd renewal of the Tennessee Risk Management Trust since its beginnings in 1987. Every renewal has seen its share of challenges as well as growth. This year is no exception although TNRMT experienced only moderate sales efforts from the for-profit insurance companies and other pools.</p>

	<p>“We are extremely pleased with the consensus opinion of our members that TNRMT is truly a good value”, said John Evans, Program manager. “The financial crisis as well as turmoil in the insurance industry has pointed up the value of strength in numbers and the wisdom of participating in a program in which the members are the owners of the program”.</p>

	<p>This was obviously borne out as the TNRMT Trustees declared a dividend of $2 million for the third consecutive year and by the success of increasing membership in both the school and other governmental entities portions of TNRMT.</p>

	<p>We are pleased to welcome Lauderdale County Schools as our 101st school member as well as Lauderdale County Government, 24th Judicial District Drug Task Force, Crockett Public Utilities, City of Decherd, McMinnville Electric, Big Creek Utility District, Crockett Mills Utility, Double Springs Utility District, Lauderdale County Highway, Tellico Area Services Systems, Tullahoma Utilities Board, McMinnville Electric, Crockett Public Utilities, North Utility District of Rhea County and South Blount Utility District making a total of 66 non-educational governmental entity members.</p>
	
	<p>With the growth experienced by TNRMT and the excellent financial strength of the program, TNRMT is well poised to be the long term solution for schools and governmental entities in Tennessee for the foreseeable future.</p>

