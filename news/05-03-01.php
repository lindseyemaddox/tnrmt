	<h1>New Trustee Election Procedure to Begin Immediately</h1>

	<h3>March, 2005</h3>

	<p>Thank you for promptly returning the intergovernmental agreements which were distributed earlier. Through these agreements, you have approved a new election procedure established by the risk management trustees that will begin immediately.</p>

	<p>Under the new election procedures, as the current trustees terms expire, new trustees will be elected by the boards which are members of the Trust. The elections will take place this year for three trustees whose terms expire June 30, 2005. Boards in these districts will be notified that they have the opportunity to nominate persons to run for these positions. Each nomination must include a résumé listing qualifications of the candidate.</p>

	<p>Nominations will be received by a nominating committee, which will nominate at least one person for each position. Each board will select one voting delegate for each program in which the board participates or two voting delegates if the board participates in the property-casualty program and the workers compensation program. Each voting delegate will receive a ballot and have the opportunity to vote for a trustee to represent the member boards in that district. The candidates elected will take office July 1, 2005.</p>
	
	<p>Every board member and superintendent that we spoke to praised the new election procedure. “It just makes sense for trustees to be elected by the membership rather than being appointed,” said Trustee Robert Stidham, chairman of the Hawkins County Board of Education. “The risk management trust belongs to the members of the trust. Trustees should be elected by the membership”.	</p>

