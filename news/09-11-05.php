	<h1>Property Coverage Enhanced for 2009 Renewal</h1>

	<h2><a href="/bios/evans.php" class="fancybox fancybox.iframe">John Evans</a>, NGU Risk Management</h2>

	<h3>November, 2009</h3>

	<p>Most, if not all, of you are aware that TNRMT has been performing a property appraisal program for the past three years. The purpose of this appraisal is to promote equity among members with assuring proper insured values while encouraging a level of comfort that member’s buildings were adequately covered.</p>

	<p>TNRMT currently covers over $8 billion in member’s buildings and contents. It is obviously important that there is enough coverage to rebuild or repair damage after a major loss. TNRMT has accomplished this in the past by providing up to 125% of the scheduled value on each building to be sure limits are sufficient. This is not the best solution but was liberal in coverage and worked well in almost all losses. The best way to assure equity and sufficient coverage is to provide BLANKET LIMITS. It was not possible to do this until appraisals were completed. Underwriters will provide the best coverage at the best price when they have confidence that covered buildings are properly valued. Our excess property underwriters now have this confidence level as a result of the appraisal program.</p>

	<p>Coverage for your buildings is now based on blanket limits. This means that the value of all buildings owned by each member will be totaled and that amount will be the available limit in the event of a loss. For example, if you are a county government and you have a courthouse valued at $2,000,000, a fire hall valued at $500,000 and a health department building valued at $1,000,000, there would be a total limit available for coverage for a loss to any individual building in the amount of $3,500,000.</p>

	<p>TNRMT will continue to look for ways to provide the best coverage to its members at the lowest possible cost while providing the most responsive service available.</p>

