	<h1>In Memory of Joyce Gregory</h1>

	<h3>March, 2005</h3>

	<p>It was just another day on the route for Joyce Gregory.</p>

	<p>A 14-year-old passenger shot Gregory, 47, a Stewart County school bus driver, teacher and mother of two, to death on March 2.</p>

	<p>In the post-9-11 world, we’ve all gained a new appreciation for the first responders that stand ready and willing to throw themselves into harm’s way so that we may be safe.</p>

	<p>We always include in this group, soldiers, policemen, firemen and EMT personnel. We seldom consider in our list of those we call “hero” the scores of other occupations that contribute to education, safety and our very way of life such as maintenance workers, teachers, principals, and now, bus drivers.</p>

	<p>It shouldn’t require a tragedy for all of us to remind ourselves that the school bus drivers, teachers, administrators and others who ensure that our children arrive safely at school and are set on a course for success as adults often go without thanks.</p>

	<p>We stand with you in mourning the loss of Joyce Gregory, and our hat is off to all of those involved with the education of our youth.</p>

