	<h1>Fire leads to AC switch recall</h1>

	<h3>May, 2006</h3>

	<p>When a fire at Macon County's West Side Elementary School in early 2004 resulted in the total loss of a portable classroom building, TNRMT was notified of the fire immediately and Claim Representative Harrold Henderson responded to the Loss.</p>

	<p>Noting the fire seemed to be of a suspicious nature, Henderson called in an expert to assist with the investigation. As a result of his efforts, and those of the reinsurer, St. Paul-Travelers, the Trust was able to recover 100% of the paid damages from the responsible party.</p>

	<p>Although it has taken two years, the investigation ultimately determined that the fire originated in an Amana brand Package Terminal Air Conditioner (PTAC) in one of the classrooms. The fire was related to a problem within the unit that can cause the heating elements to overheat and ignite materials within or adjacent to the unit.</p>

	<p>In 2004, Amana issued a "Notice of Repair Program for Commercial and Institutional Owners." Amana stated that if the air flow becomes restricted (by a plugged air filter, or furniture too close to the unit) the heating elements will get hotter and activate the safety limit switches. Over time, the safety switch can wear out due to its continuous cycling and a fire hazard can result.</p>

	<p>Amana said owners of the unit should clean or replace the filters every 30 days and replace the safety limit switch with a new one provided by the manufacturer.</p>

	<p>School systems still using the PTAC units may be at risk for a fire if the safety limit switches haven’t been replaced or the unites haven’t been properly cleaned.  Schools should check to see if these units are in use and follow instructions available at www.regcen.com/PTAC.  To find the model number, lift the unit’s front cover.  See the box below for relevant model numbers.</p>

