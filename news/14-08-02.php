	<h1>Workers Compensation Audit – Why Be Afraid?</h1>

	<h2><a href="/bios/mathis.php" class="fancybox fancybox.iframe">Jeff Mathis</a>, NGU Risk Management</h2>

	<h3>August, 2014</h3>

	<p>Most of us on the financial side of business want to run and hide when we hear the word auditor.  I know here at NGU Risk Management, I am frequently tempted to do so.   If seems that every time I turn around someone wants to audit something.  You have your own internal auditors, independent financial auditors, sales tax auditors, state auditors, workers compensation auditors, and yes, maybe even the most feared of all, the dreaded IRS auditor.  This list doesn’t include all of those voluntary audits you complete each day disguised as “can you verify this form”.  How many times do you want to yell “just leave me alone and let me do my job.”</p>

	<p>With that being said, I think you will agree that one of the least painful audits you can endure is the TNRMT workers compensation payroll audit.  If it is not the least painful it is one of the few that may actually bring you good news.</p>

	<p>For the last several years TNRMT has hired a practicing certified public account, Roger Kehrt, to conduct on site payroll audits.  Most of the time the audit produces results which are consistent with the estimate.  You may find it interesting that in the most recently completed audit period 55% of the audits came within 5% of the original estimates while 78% were within 10% of the estimates.  However, there are always a few each year where the payroll has increased or decreased substantially.  The increases are generally due to additional employees being hired in a certain department while the decrease is due to an overall reduction of employees due to outsourcing.</p>

	<p>So why be afraid of the workers compensation audit?  The purpose of the audit is merely to confirm your actual payroll numbers.  Remember, this is one of the few audits that can bring good news-a return of premium $’s!!!-in addition to confirming the good work you know you do.  In the most recent audit completed by TNRMT over 55% of the membership actually got money back.</p>

	<p>When Mr. Khert calls to schedule your audit, don’t be afraid, be happy, you may get a check in the mail.</p>

