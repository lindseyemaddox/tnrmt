	<h1>He’s not your average (Billy) Joe</h1>

	<h2>Trust board member marks 44 years in education</h2>

	<h3>February, 2007</h3>

	<p>With three schools, a steady population, a defined service area, and “the best school board in the world”, Fayetteville Director of Schools Billy Joe Evans is in an ideal situation, he believes.</p>

	<p>“I’ve never had a job I didn’t like.  This job is a very rewarding experience,” Evans said.</p>

	<p>He’s been at his post for a number of years – 19 to be exact.  And he draws on 44 years of total experience in education in his role as a trustee of the Tennessee Risk Management Trust.</p>

	<p>His job isn’t without challenges – chief among them finding top quality teachers.  “We don’t have the number of teachers going into education that we once did.  We’ve been fortunate with our good teachers, but we used to have 25 applications for every position.  Now, we have two applicants for a job,” Evans said.  It’s especially tough to hire teachers in special education and foreign language.</p>

	<p>“In a small, rural area like we are, there are more graduating and staying in the larger areas (of Murfreesboro and Nashville).  We’ve been fortunate to have some home grown people coming back.”  But when teachers do come back to work in the Fayetteville City Schools, Evans said, it’s difficult to compete with Madison County, Alabama, just across the state line.  Madison pays $5,000 more per year and Alabama allows teachers to retire with 50 percent of their pay at 25 years.  Tennessee requires a 30-year tenure for teachers to receive about half their regular salary in retirement.</p>

	<p>Fayetteville has been a Trust member since the early 1990s.  Evans said his school system has benefited from being in the Trust.</p>

	<p>“We feel confident that it’s very competitive and that we have the coverage we need.  It’s hard to compare apples to apples (among various insurance plans), but with the Trust we know what we’re getting.  We know what the coverage is and what the experience has been,” Evans said.</p>

	<p>Like most school systems, Evans said his hasn’t had any large losses, but increasing worker’s compensation claims have been a problem.</p>

	<p>“We’re trying to do more training.  We seem to have some freak things happen.  We’re trying very hard to educate our employees,” Evans said.</p>

	<p>Evans felt honored a couple of years ago when Trust members in south central Tennessee elected him a spot on the board of trustees.  He said the trust has gone through some difficult times but “we have all that behind us now.”</p>

	<p>In the two decades Evans has led the Fayetteville system, he has seen increases in money through the state’s Basic Education Program and the growth of rules and regulations with the federal No Child Left Behind program.  Folks who left the system in the 1970s and 1980s would recognize the modern classroom, “but it would certainly be different.”</p>

	<p>Fayetteville enjoys an open enrollment system and a working relationship with county officials.  About one-third of Fayetteville student come from the county.  Fayetteville has three schools; a K-3, an intermediate 4-6, and a junior high school 7-9.  Once student reach the 10th grade, thy attend Lincoln County High School.</p>

	<p>Fayetteville has been a high performing system.  In recent years, the state ranked the system in the top 10 in several subjects and grade levels.</p>

	<p>As for that “best school board in the world” designation, Evans praised the members for all possessing college degrees and never letting politics creep into decisions. </p>

	<p>“They have different opinions, but they all look out for what best for the child,” Evans said.  “We’ve only had one vote that wasn’t unanimous,” and when the majority decides a direction to take, everybody supports the decision, he said.</p>


