	<h1>An Ounce of Prevention</h1>

	<h2>January 2009</h2>

	<p>In 2004, perhaps as the result of a claim investigated by the Risk Management Trust, Amana issued a “Notice of Repair Program for Commercial and Institutional Owners for Amana brand Package Terminal Air Conditioner (PTAC). Amana markets the units under the brand name of Amana, Trane and American Standard. You will recognize these package HVAC units as those located on the vertical exterior walls of most portable classrooms and other mobile storage or office units.</p>

	<p>Amana stated that if the airflow becomes restricted (by a plugged air filter, or furniture/obstructions being too close to these units) the heating elements can get hotter and activate the safety limit switches. Over time, the safety limit switch can then wear out due to the continuous cycling and a fire hazard can result. Amana went on to say that the unit owners should clean or replace the air filters every thirty days and replace the safety limit switch with a new one provided by the manufacturer.</p>
	
	<p>Unfortunately, the Trust has been notified of yet another claim involving one of the units affected by the Notice of Repair. Please take note of this potential hazard and investigate to determine if you have any of these units in use. Please check to determine if you have these units in use. To find the model number, lift the units’ front cover and follow instructions available at www.regcen.com/PTAC.</p>

