	<h1>Hazards don’t take a vacation</h1>

	<h3>May, 2006</h3>

	<p>We at Tennessee. Risk Management Trust want to thank all of you for working hard to improve the safety of your workplace.  As we approach the summer months, with many of you preparing for summer vacations, safety must continue to be a No 1 priority. Here are some tips to help ensure your safety.</p>

	<h4>Back Injury Prevention</h4>

	<p>Use proper lifting techniques

	<ul>
		<li>Squat close to the object</li>
		<li>Bend your knees</li>
		<li>Hold object closely to body</li>
		<li>Keep back straight</li>
		<li>Lift with your legs, not with your back</li>
	</ul>
	</p>

	<p>Ask for help if it is needed to lift an object Use carts and to move objects. Don't overload boxes and trash cans. If you can't lift it, don't expect someone else to.</p>

	<h4>Chemical Safety</h4>

	<p>Know where your MSDS (material safety data sheets) are. Read labels on all chemical products and don't mix chemicals.</p>
	
	<p>Don't bring in your own household cleaners. Only trained employees should use chemicals.</p>

	<h4>Personal Protective Equipment (PPE)</h4>

	<p>Make sure you’re wearing the right PPE for the job. Know how to wear your proper safety gear Make sure it is available and easy to find.

	<ul>
		<li>Use safety glasses for mowing, weed eating, mixing chemicals and any job with flying debris</li>
		<li>Use gloves when working with chemicals, sharp objects and any blood-borne pathogens</li>
		<li>Wear proper footwear - non-slip shoes for stripping and cleaning floors and good work boots for outdoor work</li>
		<li>Protect your ears with ear plugs when working around any machinery or equipment.</li>
	</ul>
	</p>

	<p>If you are not sure of the right PPE, ask your supervisor.</p>

	<h4>Fall Hazards</h4>

	<p>Make sure to use only approved ladders and stools.  Desks \ chairs do not count!</p>

	<p>Wear proper fall protection, i.e., safety harness and lifelines, when working at heights over six feet or from lifts.</p>

	<p>Let someone know when you are going to be working from height.  Work in pairs.</p>

	<h4>Electrical Work</h4>

	<p>Make sure if electrical work is being done that lockout / tagout is being used to ensure isolation of energy.  Only trained individuals should perform this task.</p>
	
	<p>Make sure you are using extension cords that are not damaged and have a GFCI built in.</p>
	
	<p>Make sure all machine guards are in place.  Only use machines if you have been properly trained.</p>
	
	<p>Those who look out for hazards and correct them will greatly reduce the chances of anyone being injured. Don't let possible hazards in your workplace go unnoticed. Correct the situation if possible or alert your supervisor immediately so the problem can be fixed.</p>
	
	<p>Employees at all levels should take responsibility and initiative for improving safety. If you have any suggestions or questions, please contact your supervisor or loss control consultant.</p>
	
	<p>The risk management team looks forward to working with everyone to improve safety in the workplace. Your willingness to work with us is greatly appreciated. We hope you all have a great and safe summer.</p>
	
