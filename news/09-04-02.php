	<h2>April 2009</h2>

	<p>Staff members of the Risk Management Trust recently had the opportunity to offer insight into the inner workings of a governmental pool to several of Tennesseeâ€™s Superintendent Study Councils. The topic, RISK POOLING AS A FINANCIAL PRODUCT, had the objective of pointing out the differences between insurance risk pooling versus purchasing from a commercial, for profit company. Much the same as your school district or county government, the Risk Management Trust is a governmental entity, not a private vendor marketing its products. Owned by and operated for the benefit of its members, the Trust uses the collective buying power of the membership, now some 150 strong, to secure the most affordable products and services for its members. In addition, the investment income gained from reserves is used to offset the cost of coverage for members.</p>

