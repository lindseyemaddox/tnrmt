	<h1>Trust Welcomes New Trustees</h1>

	<h2>January 2009</h2>

	<p>The Trust says goodbye, and thank you to longtime Trustees, Tony Tucker and Robert Stidham. Each had contributed immensely to the success of the Risk Management Trust. Their insight and dedication will certainly be missed by all.</p>

	<p>The Trust would also like to welcome new Trustees, <a href="/bios/brown.php" class="fancybox fancybox.iframe">Denise Brown</a> of Unicoi County and Paul Ward of Carroll County.</p>

	<p>Denise has served as Director of Schools of Unicoi County since July of 2003, and has been dedicated to the students of Unicoi County for the past 22 years. Additionally, Denise serves on the Chamber of Commerce for Unicoi County, the Library Board of Directors, YMCA Board, Executive Committee of the Superintendent’s Study Council, and is Chairperson for the Superintendent’s Study Council of Upper East Tennessee. Denise’ background in educational environment will be an asset to the Trust.</p>

	<p>Paul Ward has been employed by Agri General Insurance for the past 23 years and is currently the Marketing Supervisor of the states of Tennessee, Arkansas, and Louisiana. Agri General sells and services crop insurance as well as farm property and casualty. Paul was elected to the Board of the Huntingdon Special School District in 2004. His background in insurance and service will give Paul great insight into the operations of the Trust.</p>
	
	<p>Welcome Denise and Paul.</p>

