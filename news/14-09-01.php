	<p>The Tennessee Risk Management Trust has been fortunate and of course a lot of Atta boys need be extended to our maintenance professionals as we have only experienced a single catastrophic loss in relation to bleacher failure in the last 6 years. Discussion on bleacher and grandstand safety can sometimes create confusion of what is required and why. We want to ensure everyone understands the code requirements and essentials for keeping our students and spectators safe. Interestingly enough, a majority of the bleacher related incidents reported are student athlete falls during practice drills. Is there a hill available?</p>

	<h4>Yearly Inspection is Required.</h4>

	<p>International Code Council, <a href="http://ecodes.biz/ecodes_support/free_resources/ICC_Standards/ICC_300-2012_Bleachers/ICC300-2012_main.html" target="_blank">ICC 300-2007</a>, Section 105.2 states that "Yearly inspection required. The owner shall cause all bleachers, folding and telescopic seating, and grandstands to be inspected at least once a year...."   by a qualified person.</p>

	<p>A qualified person must be trained (employed by manufacturer), a professional engineer (PE), or Registered Architect meaning your maintenance staff likely does not have the necessary credentials. Are there any "exemptions" from these requirements? NO, just in case you were wondering.</p>

	<p>The ICC 300 is in place to keep you and your community safe while decreasing liability for your facility. While it is important to perform these inspections, it is vitally important to follow through with the repairs and upgrades to avoid accidents and negligence. These requirements were put in place due to prior horrific incidents and shouldnâ€™t be taken lightly.</p>

