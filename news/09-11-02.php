	<h1>Police Officer Safety: Meth Users</h1>

	<h2><a href="/bios/stites.php" class="fancybox fancybox.iframe">Chris Stites</a>, Safety Engineering and Claims Management</h2>

	<h3>November, 2009</h3>

	<p>As dangerous as the meth lab operation is, for most police officers the danger from meth comes from dealing with the methamphetamine user. Meth users are responsible for violence toward themselves, family members, friends, strangers, and each other. Headlines such as these are unfortunately, a by-product of meth use.</p>
	
	<p>The abuse of methamphetamines is in stages, with the user being most dangerous when they are tweaking. Tweakers probably have not slept in 3-15 days and are irritable and paranoid.</p>
	
	<p>Below are the stages:
		<ul>
			<li>RUSH (5-30MINUTES)</li>
			<li>HIGH (4-16 Hours)</li>
			<li>BINGE (3-15 Days)</li>
			<li>TWEAKING</li>
			<li>CRASH (1-3 Days)</li>
			<li>NORMAL (2-14 DAYS)</li>
			<li>WITHDRAWAL (30-90 Days)</li>
		</ul>
	</p>

	<p>During the “tweaking” phase, the behavior is very unpredictable, and the effects of tweaking are intensified by other depressants, such as alcohol. The tweaker may appear normal at first glance. Their eyes are clear and their speech is concise. However, their eyes are moving at about 10 times faster than normal. There may be a quiver to their voice. Often the movements will be jerky. They may be saying weird, delusional, or paranoid things.</p>
	
	<p>If you, a police officer, encounter someone you believe is tweaking, here are a few tips that can help keep you safe:
		<ul>
			<li>Get backup in route. If violence erupts, you want your backup officers already on-scene or already in route.</li>
			<li>Maintain a reactionary gap. A seven to ten foot gap is a good minimum. This allows you slightly more time to react if the tweaker becomes violent. Almost as important, this does not “crowd” the tweaker. Keep in mind that he is paranoid, and by standing close to him, he feels crowded and his feelings of paranoia are intensified.</li>
			<li>Slow your rate of speech and lower the pitch of your voice.</li>
			<li>Slow your movements. Try to avoid any sudden moving.</li>
			<li>Avoid shining lights in his face. Bright lights can be very frightening to a tweaker. Bright lights can cause a tweaker to spontaneously fight or run.</li>
			<li>Keep the tweaker talking. If he is talking, he is not listening to the paranoid voices in his head. A silent tweaker often means his paranoia has encroached into reality. You are now part of his paranoid delusions, which could mean violence against you is imminent.</li>		
		</ul>
	</p>

	<p>Stay safe!</p>

