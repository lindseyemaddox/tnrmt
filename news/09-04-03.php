	<h1>To Bid or Not to Bid, That Is the Question</h1>

	<h2>April 2009</h2>

	<p>The current economy and shrinking tax receipts have caused everyone involved in local and state government to look for ways to use available funds in the most conservative ways. For those of us in the public sector, this is not a new concept. We have always been open to new ideas and new ways to make the most of the resources at our disposal. This was the case in 1987 when many schools could not afford insurance, and pooling was the only attractive alternative that would allow some schools to even be able to purchase insurance coverage. The Trust was created on the premise that it would be owned by its members and operated by public officials only for the benefit of those who were member/owners. It would not have to be profitable but if it was, only its member entities would receive the benefit of any profits.</p>

	<p>The TNRMT renewal is coming up again on July 1st, and many of you have seen or will see advertisements from competitors of the Trust suggesting that if you bid with a commercial carrier, they can reduce your insurance cost. Remember, commercial carriers are in business to make a profit, and to do that, they must pass the cost of operation on to you. If they make a profit, year after year, they will pass the profits back to their shareholders. On the other hand, the Trust does not have to be profitable, but rather just cover its operating cost. When the Trust does make a profit, what do they do? In the past three years, the Trust has paid or will pay more than six million dollars back to its members in the form of a dividend.</p>

	<p>As a member, you own the Trust, so if a competitor contacts you promising lower cost, consider if they have a long history of claim administration for schools. The Trust does! What does the competitor promise is covered? It would be hard to say since coverage is based on the facts of each event. If you have had a claim, you know the Trust coverage is quite broad. What is the cost involved? In these troubled economic times, why test the value of cheap insurance? Selecting an insurance program is not just about getting the lowest price; it is about getting the most for public dollars.</p>

	<p>The Trust has been operating for twenty-one years, serving the insurance needs of the majority of schools in the state was well as many other governmental entities.  So, when you consider the question whether to bid or not to bid, you should also be asking, why I should bid against proven success, especially the success of the company of which I am a co-owner.</p>

