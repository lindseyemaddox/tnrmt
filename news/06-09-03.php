	<h1>County Mayor proud of proactive stance</h1>

	<h3>September, 2006</h3>

	<p>Larry Griffin has a unique perspective on county government.  After working as ambulance service directory for five different county executives and 75-180 different county commissioners, the people of Crockett County tapped Griffin to lead them.  They just re-elected him to a second, four-year term as county mayor.</p>

	<p>“Having been in local government for 30 years gave me some insight,” Griffin said.  “There are a lot of things we’d like to do but hey, you do what’s in your limits.”</p>

	<p>In 2002, he became one of the first county government representatives on the Tennessee Risk Management Trust board.  He’s proud that the Trust expanded its service to include county departments other than just schools.  It makes budgeting easier for a limited tax base county like Crockett.  “The Trust was the least expensive bid on our insurance,” he said.</p>

	<p>What Griffin is more proud of though, is the proactive stance the Trust takes on combating accidents by providing support and training to reduce accident rates.</p>

	<p>Griffin is also happy to see a turnaround in the Trust.  “You hate to recall problem areas, but the Trust has gone from a situation of defending ourselves at every meeting to now we conduct serious business,” he said.  “Now we have a clean slate and can see about our business.”</p>

	<p>Griffin is trying to turn Crockett County around, too.  The county is sitting and waiting for a very large industrial concern to locate in its just-certified “megasite” – the 1,750 acre West Tennessee Auto Park.</p>

	<p>“We’re hoping we can attract something here to help our grandchildren stay at home and find a job,” Griffin said.</p>

	<p>Economic development is a great challenge.  “We’re not centering everything on that site, but we do have to be ready should someone come knocking on our door,” he said.  “We’re being looked at every day.”</p>

	<p>The county has allocated more money to the county Chamber of Commerce for economic development and we site development.</p>

	<p>The county just moved into a new $750,000 health department that includes a primary care facility.</p>


