	<h1>Cold Weather Preparation</h1>

	<h2><a class="fancybox fancybox.iframe" href="/bios/stites.php">Chris Stites</a>, Safety Engineer, Loss Control Specialist, Safety Engineering &amp; Claims Management</h2>

	<h3>January, 2015</h3>

	<p>Winter is upon us!  Ready or not, freezing temperatures are here.  The forecast for this week calls for below freezing temperatures across Tennessee.  Prepare today so you can avoid injuries and other problems tomorrow.</p>
	
	<p>Over the last few years, the Trust has paid out in excess of $300,000 due to winter weather.  It is of the utmost importance that plans be put in place, executed, and audited to ensure a safe well running facility during these times.  Do you have a secondary person spreading ice melt on those wet mornings when ice can form?  Here are some items that we all should consider and corresponding actions that should be taken during these winter months:</p>
	
	<ul>
		<li>Be sure heating systems are serviced and maintained so they are in good working order.</li>
		<li>Instruct employees to keep wet floors mopped during inclement weather so the floors dry more quickly.</li>
		<li>Use barricades and/or warning signs to warn of hazardous floor conditions.</li>
		<li>Keep helpful winter supplies fully stocked, including: First Aid Supplies, Snow Shovels, Ice Chippers, Ice Melting Compounds, Door Mats and/or Nonskid Runners.</li>
		<li>Make arrangements in advance to ensure prompt plowing of parking lots and driveways.</li>
		<li>Shovel and de-ice sidewalks, walkways and steps.</li>
		<li>Watch for refreezing of melted snow and ice on walkways and parking lots.</li>
		<li>Control build-up of icicles and frozen snow masses on roof overhangs, rain gutters and overhead fixtures.</li>
		<li>Maintain walkway and parking lot surfaces in good condition to prevent ice and snow-filled holes from creating concealed tripping hazards.</li>
		<li>Provide adequate lighting to illuminate parking lots, walkways and steps.</li>
		<li>Maintain accessibility of emergency areas, fire lanes, fire hydrants and fire protection equipment.</li>
		<li>Do not permit snow piles to obstruct the view of traffic entering or leaving the premises.</li>
		<li>During times of extended leave (holidays, inclement weather, weekends, etc.), make sure that the heat is set to a reasonable temperature so pipes do not freeze and burst.</li>
	</ul>
	
