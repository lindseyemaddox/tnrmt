	<h1>Attorney General Issues Ruling Requested by TSBA</h1>

	<h3>September, 2005</h3>

	<p>TSBA leaders did not agree with the opinion of RMT trust attorney Keith Dennen that Tennessee law precluded a private non-profit corporation such as TSBA from membership in the Risk Management Trust because it is not a governmental entity. Their attorney had told them that: “TSBA is clearly a political subdivision of the State of Tennessee and as such, is eligible not only to participate in but also form and operate an intergovernmental insurance pool." So, they asked the Attorney General for an official opinion.</p>

	<p>On Tuesday, August 29, the AG issued an opinion which was in agreement with that of the Trust attorney. The last paragraph of the AG opinion included the following language:”…TSBA is not a governmental entity under Tenn. Code Ann. 29-20-401 and can not create a reserve or special fund, or enter into a pooling agreement, as authorized under this section.”</p>

