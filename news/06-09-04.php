	<h1>New Trust member knows kids, finance</h1>

	<h3>September, 2006</h3>

	<p><a href="/bios/lacey.php" class="fancybox fancybox.iframe">Frank Lacey</a> isn’t strident in his views, just settled.  “I have a passion for getting our kids back to the root of education and also equipping teachers and giving them the ability to actually teach and take back their classrooms.”</p>

	<p>He knows about children, he has four.</p>

	<p>Lacey knows about money too.  He’s branch manager of Community South Bank in Adamsville.  “I understand how money works and how it flows.”</p>

	<p>So when McNairy County school board member saw an opportunity to serve, he sought a seat on the Tennessee Risk Management Trust board and won.</p>

	<p>Lacey was just re-elected to his second, four-year term on his local school board.  He joined the Trust board in September.</p>

	<p>“I’m not an advocate of big reform, but I would like to see the counties and states take over their schools more,” and federal government’s role be diminished, Lacey said.  He mentioned No Child Left Behind as an example of invasive federal interference.  “It doesn’t take small communities into consideration.  But sadly, it the states and counties had been doing their jobs, the feds wouldn’t have come in.”</p>

	<p>Lacey said McNairy County, like other school systems, is facing the challenge of “getting parents to understand it takes communication, commitment of time and money, and support” for local schools to thrive.  He pointed out that one of the first things relocating companies look at “is what kind of schools you have.  If they’re not good, the company is not going to look upon you as favorably.  We’re trying to convince parents and county commissioners of the importance of education.”</p>

	<p>Lacey said he is most proud of the efforts to get a school building program going.  Property taxes are low, and McNairy is getting an influx of people from larger metropolitan areas.  “They’re getting out of the high rent, high crime rate areas.  The county’s growing,” he said.</p>

	<p>The banker considers it an honor to be elected to the Trust.  “I consider it a part of citizenship,” he said.</p>

	<p>“With his background in banking and finance and his experience as a McNairy County school board member, he will be an excellent member of our board of trustees,” said Mark Farley, chairman of the TNRMT board.</p>


