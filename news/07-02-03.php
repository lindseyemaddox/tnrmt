	<h1>How does the pool work and why should we stay in it?</h1>

	<h2>by <a href="/bios/evans.php" class="fancybox fancybox.iframe">John Evans</a>, NGU Risk Management</h2>

	<h3>February, 2007</h3>

	<p>Membership in TNRMT is generally stable from year to year but as school board members and directors change, some of our members will soon discuss whether to “bid” their “insurance”.</p>

	<p>The terms “bid” and “insurance” are in quotes as they are terms commonly used in the risk management and purchasing process.  The term “insurance” is defined as the act of transferring liability for potential loss to another by contract.</p>

	<p>Although this is a universal term, it does not specifically define what your Tennessee Risk Management Trust (TNRMT) does for its members. The TNRMT is a pool which is owned collectively by its members who "pool" their resources to cover each other's losses without consideration to profit. This is unlike "insurance" which is a company generally owned by stockholders motivated by profit.</p>

	<p>A pool requires all its owners/members to deposit a predetermined amount of money into the pool. The amount is determined by the best estimation that can be made of the money needed to cover anticipated losses. If there are more losses than money, an outstanding liability exists for all members. If there is more money than losses, then the surplus is owned by all members to be used to offset future deposits that members would otherwise be required to make.</p>

	<p>A pool is designed with just one objective: to provide the best possible product at the best possible price. There is no profit motive because a pool serves only the interests of its members, not stockholders or the general public. A pool's appetite for money is governed only by the losses of its members. The net result is that there is no better product available.</p>

	<p>The term "bid" is another universally used term. The bid process is normally used to find the best price from one of several vendors on a certain commodity. The only thing wrong with bidding for insurance is that insurance is not a commodity. Insurance is a financial vehicle used to accomplish financial security while stabilizing a budget. A pool is one step further from a commodity than is insurance. A pool is owned by its membership. The members keep any surplus that is not needed for claims. It doesn't get any better than that.</p>

	<p>Every member has a bad year occasionally. For some, it occurs every three or four years For others, it may take six or eight years but the bad loss will certainly occur for every member at some point. When a member has a bad loss year, the other members' deposits stand to pay for the losses. Then, the member who had the bad losses last year will stand for others with bad losses in following years. The result is that the cost of losses is stabilized for all members over a long period of time.</p>

	<p>The worst thing that can happen to a pool is for its membership to fluctuate from year to year. Imagine if half of the pool's members had losses of 200% of the deposit they paid this year and then left the pool at the end of the year This destabilizing effect caused by unknowing pool members only serves to hurt all involved This is why it is so important to understand the concept and function of your pool. TNRMT's long term success depends on your knowledge and teamwork.</p>


