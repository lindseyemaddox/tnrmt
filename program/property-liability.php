		<h2>While most insurance companies have a claims staff who process large numbers and differing type of claims, TNRMT employs in-house claim professionals who are dedicated to the specific needs of Tennessee public entity claims.  Therefore, the TNRMT claims staff are able to give each claim more dedicated attention than typical claims professionals at traditional insurance companies.</h2>

		<p>For each incident reported, the TNRMT adjuster will follow a tested, well established process developed over TNRMT’s long history.  After the TNRMT adjuster acknowledges receipt of the reported incident by phone and/or email, the adjuster will then begin the investigative process.  This process involves contacting all involved parties, verifying the details of the incident, and determining the financial exposure to the Trust.  After obtaining all relevant facts and documentation, the adjuster will attempt resolution of the claim based upon their extensive expertise and similar historical claims.  In addition, the adjuster will take into account not only the financial impact that the claim may have, but also what the final disposition may have on established policies of the member with regard to future claims.</p>
		<p>From time to time, adjusters are not able to resolve claims, and those claims develop into lawsuits against the member.  In this event, the case is then assigned to an attorney from the TNRMT defense network.  Attorneys in our defense network are very familiar with the TNRMT defense strategy, but more importantly, all of the TNRMT attorneys have extensive experience in public entity claims.  If a claim does develop into a lawsuit, the adjuster will not abandon the claim entirely to the attorney, unlike adjusters that work in different organizations.  TNRMT adjusters always remain involved in each claim from start to finish in order to assure the most satisfactory result for the Trust and, more importantly, our member.</p>
		<p>Our Property and Liability coverages operate under a deductible program.  Each year, members may choose their deductibles for each line of coverage based on their budget, loss history, and many other considerations unique to each member.  Guidance in selecting the appropriate deductible for each line of coverage for your entity is available from NGU Risk Management.  If a loss occurs, each member is responsible for expenses up to the selected deductible.  TNRMT is responsible for expenses that occur beyond the member’s deductible.</p>
