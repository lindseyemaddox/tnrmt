		<h1>Our Workers&lsquo; Compensation Program</h1>

		<h2>The Workers&lsquo; Compensation Program offered by TNRMT meets all Tennessee State Law requirements as well as Federal requirements.  The program addresses wage replacement benefits, medical treatment, vocational rehabilitation, and other benefits.</h2>

		<p>Under Worker’s Compensation law, income benefits will be paid at the rate of 66 2/3% (sixty-six and two-thirds percent) of the average wage of the injured employee if the injured employee is required to miss more than seven days due to the injury.  The TNRMT adjuster will advise you of the information needed to process a claim for income benefits.  Under our program, income benefits are usually paid every two weeks.</p>
		
		<p>The Workers’ Compensation Act allows the employer to pre-designate at least three independent physicians that are not associated together in practice to treat the employee.  You should already have a list of medical facilities in your community, but if you need assistance with the selection process, contact the TNRMT Workers’ Compensation Department at 1-888-743-4336.  If the injured worker feels medical treatment is necessary, you must give the worker a copy of form C42 which lists the pre-designated physicians.  You must then have the worker sign the form (except in cases of emergency) and forward a copy of the signed form to TNRMT.  If you have complied with the requirements of the Workers’ Compensation Act, the injured employee is obligated to accept medical care from the list of approved facilities.  If they do not, it is possible that the charges will not be paid under Workers’ Compensation.</p>

		<p>Even though our adjuster will contact the injured employee on a frequent basis to ascertain medical progress, etc., we have found it extremely beneficial for a representative of the employer to maintain periodic contact with the employee as well.  Although the adjuster will contact your representative periodically to provide status reports, we ask that you to call TNRMT if you become concerned about the status of the claim.  Only with your assistance can we contain the ever-increasing cost of Workers’ Compensation claims.</p>

